-- ─────────────────────────────────────────────────────────────────────────────
-- DAP configuration for C/C++ — uses cmake-tools launch target when available,
-- otherwise prompts for executable path. Called from dap setup.
-- ─────────────────────────────────────────────────────────────────────────────

function M.setup_dap()
  local ok, dap = pcall(require, "dap")
  if not ok then return end

  local function pick_executable()
    local ok_cmake, cmake = pcall(require, "cmake-tools")
    if ok_cmake then
      local target_path = cmake.get_launch_target_path and cmake.get_launch_target_path()
      if target_path and target_path ~= "" and vim.fn.executable(target_path) == 1 then
        return target_path
      end
    end
    return vim.fn.input("Path to executable: ", vim.fn.getcwd() .. "/", "file")
  end

  local function parse_args()
    local raw = vim.fn.input("Program arguments (space-separated): ")
    if raw == "" then return {} end
    return vim.split(raw, "%s+")
  end

  local cpp_config = {
    {
      name = "Launch (codelldb, cmake target)",
      type = "codelldb",
      request = "launch",
      program = pick_executable,
      args = parse_args,
      cwd = "${workspaceFolder}",
      stopOnEntry = false,
      console = "integratedTerminal",
    },
    {
      name = "Attach to process (codelldb)",
      type = "codelldb",
      request = "attach",
      pid = function() return require("dap.utils").pick_process() end,
      cwd = "${workspaceFolder}",
    },
  }

  dap.configurations.cpp = cpp_config
  dap.configurations.c = cpp_config
end

-- ─────────────────────────────────────────────────────────────────────────────
-- Neotest adapters — extensible list. Add adapters by editing this function.
-- ─────────────────────────────────────────────────────────────────────────────

function M.neotest_adapters()
  local adapters = {}

  local ok_vimtest, vim_test = pcall(require, "neotest-vim-test")
  if ok_vimtest then
    -- Default vim-test C/C++ runner. Catch2 is the user's primary framework;
    -- override per project via :let g:test#cpp#runner = '...' or .vim-test config.
    vim.g["test#cpp#runner"] = vim.g["test#cpp#runner"] or "catch2"
    vim.g["test#c#runner"]   = vim.g["test#c#runner"]   or "catch2"

    table.insert(adapters, vim_test({
      -- Neotest uses neotest-vim-test only for filetypes other adapters don't cover.
      ignore_file_types = { "python", "lua", "rust", "go", "java", "javascript", "typescript" },
    }))
  end

  return adapters
end

-- ─────────────────────────────────────────────────────────────────────────────
-- cmp source list for C/C++. Includes treesitter so keywords (private, public,
-- noexcept, constexpr, …) and project identifiers come through even when
-- clangd doesn't propose them in the current context.
--
-- Other languages keep the global cmp config from lua/plugins/completions.lua.
-- ─────────────────────────────────────────────────────────────────────────────

function M.setup_cmp_filetypes()
  local ok, cmp = pcall(require, "cmp")
  if not ok then return end

  local sources_top = {
    -- Filter out clangd's `Snippet` items (kind 15) — luasnip handles snippets.
    { name = "nvim_lsp", priority = 1000,
      entry_filter = function(entry) return entry:get_kind() ~= 15 end },
    { name = "luasnip",    priority = 800 },
    { name = "treesitter", priority = 600, keyword_length = 1 },
    { name = "path",       priority = 500 },
  }
  local sources_fallback = {
    { name = "buffer", priority = 250, keyword_length = 2 },
  }

  for _, ft in ipairs({ "c", "cpp", "objc", "objcpp", "cuda" }) do
    cmp.setup.filetype(ft, {
      sources = cmp.config.sources(sources_top, sources_fallback),
    })
  end
end

-- ─────────────────────────────────────────────────────────────────────────────
-- Public setup
-- ─────────────────────────────────────────────────────────────────────────────

function M.setup()
  M.setup_server()
  M.setup_autocmds()

  -- cmp.setup.filetype is global, so it's safe to call once at startup
  -- (nvim-cmp from completions.lua loads eagerly).
  vim.api.nvim_create_autocmd("VimEnter", {
    once = true,
    callback = function() M.setup_cmp_filetypes() end,
  })
end

return M
